# 1. Compute average expression of each marker gene across all clusters
compute_avg_expr <- function(
    obj,
    marker_genes,
    group.by = "seurat_clusters",
    assay = "RNA",
    slot = "data"
) {
  marker_genes <- intersect(marker_genes, rownames(obj))
  # https://satijalab.org/seurat/reference/averageexpression
  avg_expr <- AverageExpression(
    obj,
    features = marker_genes,
    group.by = group.by,
    assays = assay,
    slot = slot
  )[[assay]]
  
  return(avg_expr)
}

# 2. For each gene, find the cluster that express it the most
find_top_cluster_per_gene <- function(avg_expr) {
  data.frame(
    gene = rownames(avg_expr),
    top_cluster = apply(avg_expr, 1, function(x) colnames(avg_expr)[which.max(x)]),
    max_avg_expr = apply(avg_expr, 1, max),
    row.names = NULL
  )
}

# 3. For each cluster, list top marker genes
find_top_markers_per_cluster_from_avg <- function(avg_expr, top_n = 10) {
  res <- lapply(colnames(avg_expr), function(cl) {
    vals <- avg_expr[, cl]
    vals <- sort(vals, decreasing = TRUE)
    data.frame(
      cluster = cl,
      gene = names(vals),
      avg_expr = as.numeric(vals)
    ) |>
      head(top_n)
  })
  
  bind_rows(res)
}

# 4. Scale avg_expr by gene across clusters
scale_avg_expr_by_gene <- function(avg_expr) {
  scaled <- t(scale(t(avg_expr)))
  return(scaled)
}

# 5. Score clusters by marker sets
score_clusters_by_marker_sets <- function(avg_expr, marker_sets, na.rm = TRUE) {
  res <- lapply(names(marker_sets), function(ct) {
    genes <- intersect(marker_sets[[ct]], rownames(avg_expr))
    if (length(genes) == 0) return(NULL)
    
    data.frame(
      cell_type = ct,
      cluster = colnames(avg_expr),
      score = colMeans(avg_expr[genes, , drop = FALSE], na.rm = na.rm),
      n_markers_used = length(genes),
      row.names = NULL
    )
  })
  
  bind_rows(res)
}

# 6. Pick best label per cluster from score table
predict_cluster_labels_from_scores <- function(score_table) {
  score_table |>
    group_by(cluster) |>
    slice_max(order_by = score, n = 1, with_ties = FALSE) |>
    ungroup()
}

# 7. Wrapper: raw avg_expr + scaled avg_expr + predictions
analyze_cluster_labels_from_markers <- function(
    obj,
    marker_genes,
    marker_sets,
    group.by = "seurat_clusters",
    assay = "RNA",
    slot = "data",
    top_n = 10
) {
  avg_expr <- compute_avg_expr(
    obj = obj,
    marker_genes = marker_genes,
    group.by = group.by,
    assay = assay,
    slot = slot
  )
  
  top_cluster_per_gene <- find_top_cluster_per_gene(avg_expr)
  top_markers_per_cluster <- find_top_markers_per_cluster_from_avg(avg_expr, top_n = top_n)
  
  raw_scores <- score_clusters_by_marker_sets(avg_expr, marker_sets)
  raw_predictions <- predict_cluster_labels_from_scores(raw_scores)
  
  avg_expr_scaled <- scale_avg_expr_by_gene(avg_expr)
  scaled_scores <- score_clusters_by_marker_sets(avg_expr_scaled, marker_sets)
  scaled_predictions <- predict_cluster_labels_from_scores(scaled_scores)
  
  return(list(
    avg_expr = avg_expr,
    avg_expr_scaled = avg_expr_scaled,
    top_cluster_per_gene = top_cluster_per_gene,
    top_markers_per_cluster = top_markers_per_cluster,
    raw_scores = raw_scores,
    raw_predictions = raw_predictions,
    scaled_scores = scaled_scores,
    scaled_predictions = scaled_predictions
  ))
}

